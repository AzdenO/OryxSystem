
/////////////////////////////////////////////////////////////////////////////////////////
class TreeNode{

    /////////////////////////////////////////////////////////////////////////////////////

    constructor(children=null, val){
        this.children = children;
        this.value = val;
    }
    /////////////////////////////////////////////////////////////////////////////////////
    setChildren(children){
        this.children = children;
    }
    /////////////////////////////////////////////////////////////////////////////////////
    appendChild(child){
        this.children.push(child);
    }
    ////////////////////////////////////////////////////////////////////////////////////
}
