/**
 * @class Log
 * @description Standardised log object that each service can instantiate and register with the system console
 * for more centralised logging of system events
 * @version 0.1
 * @author DRAW
 */
///////////////////////////////////////////////////////////////////////////////////////////////////////
export default class Log{

    //////////////////////////////////////////////////////////////////////////////////////////////////
    constructor(args) {
        this.service = args[0];
        this.level = args[1];
        this.msg = args[2];
    }
    //////////////////////////////////////////////////////////////////////////////////////////////////
}